# operasi aritmatika
a = 10
b = 3

hasil = a + b
print(hasil)

hasil = a - b
print(hasil)

hasil = a / b
print(hasil)

hasil = a * b
print(hasil)

# pangkat
hasil = a ** b
print(hasil)

# mod
hasil = a % b
print(hasil)

# flor
hasil = a // b
print(hasil)

